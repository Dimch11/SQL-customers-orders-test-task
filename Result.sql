CREATE TABLE "Customers" (
	"Id"	INTEGER NOT NULL UNIQUE,
	"Name"	TEXT NOT NULL,
	PRIMARY KEY("Id" AUTOINCREMENT)
);

CREATE TABLE "Orders" (
	"Id"	INTEGER NOT NULL UNIQUE,
	"CustomerId"	INTEGER NOT NULL,
	PRIMARY KEY("Id" AUTOINCREMENT),
	FOREIGN KEY("CustomerId") REFERENCES "Customers"("Id") ON UPDATE CASCADE ON DELETE CASCADE
);

